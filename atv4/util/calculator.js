module.exports = {
    sum: function(a, b) {
        return Number.parseFloat(a) + Number.parseFloat(b);
    },

    multiply: function(a, b) {
        return Number.parseFloat(a) * Number.parseFloat(b);
    },

    subtract: function(a, b) {
        return Number.parseFloat(a) - Number.parseFloat(b);
    },

    divide: function(a, b) {
        return Number.parseFloat(a) / Number.parseFloat(b);
    },
}